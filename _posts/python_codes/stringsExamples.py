# String Declaration
s = "Hello, World!"

# Concatenation
s1 = "Hello"
s2 = "World"
s3 = s1 + ", " + s2 + "!"

# Slicing
print(s[7:12])

# Replacing
s = s.replace("World", "Python")

# Formatting
name = "John"
age = 30
s = "My name is {} and I am {} years old".format(name, age)

# Length
print(len(s))

# Case conversion
print(s.upper())
print(s.lower())
